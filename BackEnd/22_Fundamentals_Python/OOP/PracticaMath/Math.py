class Math:
    @staticmethod
    def suma( num1, num2 ):
        return num1 + num2

    @staticmethod
    def resta( num1, num2 ):
        return num1 - num2
    
    @staticmethod
    def multiplica( num1, num2 ):
        return num1 * num2

    @staticmethod
    def divide( num1, num2 ):
        return num1 / num2